               <h5 align="center">Rationale</h5>
<p align="center"><i>“The Iloilo National High School believes that quality education breaks the 
cycle of poverty—a far nobler thought that propels one and all 
not only to strive and excel but to strive, excel 
and again-strive.” Anonymous</i></p>
<p class="p1"> The ratification and establishment of the Republic Act 10533 (Enhanced Basic Education Act  of 2013) also known as the K to 12 Curriculum Program of the DepEd challenges the INHS to pursue a well- mean  educational development. This Act mandated the full enhancement and lengthening of Basic Education in the country vis a vis the implementation of the Senior High School (SHS). The school year 2016-2017 will be a banner year for SHS as it will officially implement nationwide Grade 11 and SY 2017-18 for Grade 12. These two grade levels were added to the traditional 10 year educational period for Filipinos.</p>
<p class="p1"> The INHS, as proposed, will offer Academics, Technology-Vocational – Livelihood (TVL), Arts & Design and Sports Tracks under the Senior High School program. However, it offers in a limited strand offerings under the TVL, specifically, ICT (Computer Hardware Servicing and Programming), Home Economics (Food and Beverage Service, Bread and Pastry, Cookery, Household Services, Housekeeping and Dressmaking/Tailoring). INHS is one of the 16 schools to implement the STEM strand under the Academic Track in the Division of Iloilo. It is the only school as of the moment to offer the Sports and Arts & Design Tracks under the said Division.</p>
<p class="p1">This SHS Strategic and Implementation Plan is made possible through the collective efforts of the School Principal, Dr. Nordy D. Siason Jr., all Department and Program Heads, Task-Force SHS, Personnel, Accounting and Supply Sections, Guidance Office and in consultation with the Federated PTA, Student Government, School Governing Council and LGU. The manuscript was prepared by the Senior High School Management Team. Special recognition to Mrs. Rhoda Maravillosa (retired) for her significant role in crafting the SHS Plan.</p>
